def get_robot_arm_profile(profile_name: str) -> RobotArmProfile | None:
    normalized = profile_name.strip().lower()
    for profile in builtin_robot_arm_profiles():
        names = (profile.profile_name, *profile.robot_description_aliases)
        if normalized in {name.lower() for name in names}:
            return profile
    return None
