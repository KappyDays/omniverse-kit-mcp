def _robot_usd(*parts: str) -> str:
    return resolve_catalog_asset_url("robots", f"Robots/{'/'.join(parts)}")


def _profile(
    profile_name: str,
    display_name: str,
    vendor: str,
    family: str,
    asset_url: str,
    robot_description: str | None,
    robot_description_aliases: tuple[str, ...],
    gripper_kind: str,
    built_in_gripper: bool,
    controller_strategy: str,
    support_status: str,
    support_reason: str,
    evidence: tuple[str, ...],
    max_grasp_width_m: float | None = None,
    fit_clearance_m: float = 0.005,
) -> RobotArmProfile:
    return RobotArmProfile(
        profile_name=profile_name,
        display_name=display_name,
        vendor=vendor,
        family=family,
        asset_url=asset_url,
        robot_description=robot_description,
        robot_description_aliases=robot_description_aliases,
        gripper_kind=gripper_kind,
        built_in_gripper=built_in_gripper,
        controller_strategy=controller_strategy,
        support_status=support_status,
        support_reason=support_reason,
        evidence=evidence,
        max_grasp_width_m=max_grasp_width_m,
        fit_clearance_m=fit_clearance_m,
    )


